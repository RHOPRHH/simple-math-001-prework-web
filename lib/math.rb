def addition(num1, num2)
  add =num1 + num2
  add
end

def subtraction(num1, num2)
  subt = num1 - num2
  subt
end

def division(num1, num2)
  div = num1 / num2
  div
end

def multiplication(num1, num2)
  mul = num1 * num2
  mul
end

def modulo(num1, num2)
  mod = num1 % num2
  mod
end

def square_root(num)
  sr = Math.sqrt(num)
  sr
end

def order_of_operation(num1, num2, num3, num4)
  oos =  num1 + (( num2 * num3 ) / num4 )
  oos
end
